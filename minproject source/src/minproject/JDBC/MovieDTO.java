package minproject.JDBC;

public class MovieDTO {
	private int mov_code;
	private String mov_title;
	private String mov_date;
	private String mov_genre;
	private int mov_time;
	private String mov_age;
	private String mov_rating;
	private String mov_scenario;
	private String mov_img;
	private String mov_actor;
	private String mov_director;

	public MovieDTO() {
		
	}

	public int getMov_code() {
		return mov_code;
	}

	public void setMov_code(int mov_code) {
		this.mov_code = mov_code;
	}

	public String getMov_title() {
		return mov_title;
	}

	public void setMov_title(String mov_title) {
		this.mov_title = mov_title;
	}

	public String getMov_date() {
		return mov_date;
	}

	public void setMov_date(String mov_date) {
		this.mov_date = mov_date;
	}

	public String getMov_genre() {
		return mov_genre;
	}

	public void setMov_genre(String mov_genre) {
		this.mov_genre = mov_genre;
	}

	public int getMov_time() {
		return mov_time;
	}

	public void setMov_time(int mov_time) {
		this.mov_time = mov_time;
	}

	public String getMov_age() {
		return mov_age;
	}

	public void setMov_age(String mov_age) {
		this.mov_age = mov_age;
	}

	public String getMov_rating() {
		return mov_rating;
	}

	public void setMov_rating(String mov_rating) {
		this.mov_rating = mov_rating;
	}

	public String getMov_scenario() {
		return mov_scenario;
	}

	public void setMov_scenario(String mov_scenario) {
		this.mov_scenario = mov_scenario;
	}

	public String getMov_img() {
		return mov_img;
	}

	public void setMov_img(String mov_img) {
		this.mov_img = mov_img;
	}

	public String getMov_actor() {
		return mov_actor;
	}

	public void setMov_actor(String mov_actor) {
		this.mov_actor = mov_actor;
	}

	public String getMov_director() {
		return mov_director;
	}

	public void setMov_director(String mov_director) {
		this.mov_director = mov_director;
	}
}
